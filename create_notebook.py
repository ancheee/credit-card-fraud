"## 12. Autoencoder -- Unsupervised Detekcija Anomalija\n"
"\n"
"### Potpuno drugaciji pristup!\n"
"\n"
"Autoencoder koristi **unsupervised** pristup:\n"
"\n"
"1. Trenira se **SAMO na legitimnim transakcijama** -- uci sta je 'normalno'\n"
"2. Za svaku novu transakciju, pokusava je rekonstruisati\n"
