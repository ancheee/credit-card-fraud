"    # Arhitektura po sloju: Dense -> BatchNormalization -> ELU -> Dropout\n"
"    #\n"
"    # BatchNormalization : stabilizuje treniranje, ubrzava konvergenciju\n"
"    # ELU aktivacija     : izbegava dying neuron problem ReLU-a, glatke derivacije\n"
"    # L2 regularizacija  : penalizuje velike tezine (sprecava overfitting)\n"
